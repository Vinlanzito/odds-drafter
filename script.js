function renderPlayers() {
    const playersHTML = currentPlayers.map((player) => {
    return `        
      <tr class="${player.tier % 2 === 0 ? 'even-tier ' : ""}${player.drafted ? 'drafted ' : ""}${(hideDraftedPlayers && player.drafted) || (player.fantasyScore === 0) ? 'hide' : ""}">
        <td>
            ${player.rank}
            <button class="draft-button hide">${player.drafted ?"Undraft" : "Draft"}</button>
        </td>
        <td><div class="player-line"><img src="images/${getImageName(player.name)}.png"><span>${player.name}</span> (${player.team}) &#x2022; ${player.position}</div></td>
        <td>${player.value}</td>
        <td>${player.tier}</td>
        <td>${player.adp}</td>
        <td>${player.bye}</td>
        <td>${player.injury}</td>
        <td style="background-color: ${getScarcityColor(player.scarcity)};">${player.scarcity}</td>
    </tr>
    `
  }).join("");

  tableBody.innerHTML = playersHTML;

  const rows = tableBody.querySelectorAll("tr");
  rows.forEach((row) => row.addEventListener('click', () => highlightRow(row)));
  rows.forEach((row) => row.querySelector("td").querySelector("button").addEventListener('click', row.classList.contains("drafted") ? undraftPlayer : draftPlayer));
}

function getImageName(inputString) {
    return inputString.replace(/[^a-zA-Z0-9\s\-]/g, "").replaceAll(" ", "-").toLowerCase();
}

function getScarcityColor(scarcity) {
    let ratio = scarcity.replace("%" ,"") / 100;
    let redValue;
    let greenValue;
    let blueValue;

    if (ratio > 0.5) {
        ratio = (ratio - 0.5)*2;
        redValue = Math.round(255 - 255*ratio);
        greenValue = Math.round(235 - 113*ratio);
        blueValue = Math.round(59 - 8*ratio);
    } else {
        ratio *= 2;
        redValue = Math.round(200 + 55*ratio);
        greenValue = Math.round(16 + 219*ratio);
        blueValue = Math.round(46 + 13*ratio);
    }

    return `rgb(${redValue},${greenValue},${blueValue})`;
}

function positionClick(position) {
    currentPositionButton.classList.remove('current-position');
    currentPositionButton = document.getElementById(`${position}-button`);
    currentPositionButton.classList.add('current-position');

    filterByPosition(position);
}

function filterByPosition(position) {
    currentPlayers = allPlayers.filter((player) => position === "all" || position.toUpperCase() === player.position);
    renderPlayers();
}

